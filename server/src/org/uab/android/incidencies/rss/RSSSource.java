package org.uab.android.incidencies.rss;

public class RSSSource {

	private int lineId;
	private String url;
	
	public int getLineId() {
		return lineId;
	}
	
	public void setLineId(int id) {
		this.lineId = id;
	}
	
	public String getUrl() {
		return url;
	}
	
	public void setUrl(String url) {
		this.url = url;
	}
}
